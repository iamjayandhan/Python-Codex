a="rooCk"                             #syntax and its use are given below :-
print(a.capitalize())                 #capitalizes only first letter
print(a.swapcase())                   #converts uppercase letter into lower and lower into upper
print(a.count('r'))                   #count(str,[beg,end]) #counts the occurences of given letter
print(a.index('k'))                   #finds the index of the given letter
print(a.casefold())                   #converts all letters into lower case
print(a.endswith('r'))                #checks if end letter of the word is equal to given letter
print(a.find('C'))                    #finds the index of the given letter,if not then gives -1
print(a.isalnum())
print(a.isalpha())
print(a.expandtabs())
print(a.isascii())
print(a.format_map('C'))